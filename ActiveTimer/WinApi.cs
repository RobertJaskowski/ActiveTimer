        public static StringBuilder GetWindowTitle()
        {
            var i = GetForegroundWindow();
            int length = GetWindowTextLength(i);
            StringBuilder sb = new StringBuilder(length + 1);
            GetWindowText(i, sb, sb.Capacity);
            return sb;
        }

